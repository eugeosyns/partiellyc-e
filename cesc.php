        <h3><i class="fa-solid fa-message"></i>Le CESC</h3>
        <p>Le comité d’éducation à la santé et à la citoyenneté (CESC) s’inscrit dans le pilotage de chaque
            établissement
            scolaire du second degré, conformément aux dispositions des articles R 421-46 et 421-47 du Code l’éducation.
            Il
            est une instance de réflexion, d’observation et de proposition qui conçoit, met en œuvre et évalue un projet
            éducatif en matière d’éducation à la citoyenneté et à la santé et de prévention de la violence, intégré au
            projet d’établissement. Le CESC organise le partenariat en fonction des problématiques éducatives à traiter.
        </p>

        <p><strong>Missions</strong></p>

        <p>contribuer à l’éducation à la citoyenneté,
            préparer le plan de prévention de la violence,
            proposer des actions pour aider les parents en difficultés et lutter contre l’exclusion,
            définir un programme d’éducation à la santé et à la sexualité et de prévention des conduites addictives.</p>

        <p><strong>Composition</strong></p>

        <p>personnels d’éducation, sociaux et de santé de l’établissement,
            représentants des personnels enseignants,
            représentants des parents,
            représentants des élèves,
            représentants de la commune et de la collectivité de rattachement.</p>

        <p>Représentants des personnels enseignants, des parents et des élèves sont désignés par le chef d’établissement
            sur proposition des membres du conseil d’administration appartenant à leurs catégories respectives.</p>

        <p><strong>Partenaires</strong></p>

        <p>Le CESC peut associer à ses travaux les partenaires compétents susceptibles de contribuer utilement à la
            politique éducative et de prévention de l’établissement.</p>

        <p><strong>Présidence</strong></p>

        <p>Le CESC est présidé par le chef d’établissement.</p>

        <p>Texte officiel : <a href="http://www.education.gouv.fr/bo/2006/45/MENE0602019C.html">education.gouv</a></p>
