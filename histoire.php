        <h3><i class="fa-solid fa-message"></i>L'histoire d lycée</h3>
        <p><strong>A l’origine était un champ de luzerne ...</strong><br><br>

            L’idée d’implanter un établissement secondaire public sur le territoire de la ville de Marcq était déjà bien
            présente avant que n’éclate la Seconde Guerre Mondiale, l’Histoire ne fit que retarder l’échéance et la
            flamme
            laïque se raviva dès les années 50. Quelques classes du secondaire virent le jour dans les locaux de l’Ecole
            primaire Victor Hugo. Tentative éphémère qui se réitère en 1957 dans les locaux du Groupe scolaire
            Clémenceau.
            Le rachat, par la municipalité, que dirige alors M.Robichez, d’un terrain sis Avenue du Docteur Calmette
            allait
            concrétiser le vieux rêve laïque. Au printemps 1959 débutent les travaux de construction d’un établissement
            secondaire public en lieu et place d’un champ de luzerne</p>
        <p><strong>…qui fit s’éclore l’esprit « public ».</strong><br><br>

            Les travaux s’effectuèrent à bonne allure et en octobre 1959 l’ouverture du premier bloc du Lycée permit
            d’accueillir 6 classes de la Sixième à la Quatrième dans ce qui s’appelait encore "annexe du Lycée d’état de
            garçons de Tourcoing" dont M.Colbeau est le Proviseur.
            Un ancien élève nous rappelle que " celui-ci ne manquait jamais de venir à chaque
            fin de trimestre dans toutes les classes, avec Monsieur Kernanec, pour l’énoncé public des récompenses
            ("félicitations", "encouragements", "inscription au tableau d’honneur") mais aussi pour celui des
            "reproches".... avec souvent un commentaire cinglant !"
            En 1960, M.Yves Kernanec est nommé Censeur-Directeur d’un établissement fréquenté par une centaine d’élèves.
            Entre 1962 et 1963 sont construits les deuxième et troisième blocs, le bâtiment de la demi-pension, le
            gymnase et les terrains de sport. Un lycée vient de naître, d’autant plus qu’un second cycle du secondaire
            est venu s’ajouter aux classes du collège. L’année scolaire 1963-1964 voit la première promotion de
            bacheliers du lycée, celle qui connut les vicissitudes d’un établissement en construction. En 1965
            l’établissement connaît sa première structure administrative complète comptant un proviseur (M.Kernanec), un
            censeur, un intendant, et des secrétaires. Le 14 février de cette année vit l’inauguration du Lycée mixte
            d’Etat par Monsieur le Recteur Guy Debeyre en présence de Monsieur Treffel, Inspecteur d’Académie, de
            Monsieur le Sénateur Maurice Schumann et de Monsieur Georges Lambrecq, maire de Marcq en Baroeul qui
            disparaîtra accidentellement en 1968. Cette inauguration marque la consécration d’un projet déjà bien ancien
            qui correspond aussi à une forte poussée démographique puisque la population marcquoise est passée de 27000
            à 35000 habitants entre 1960 et 1969.</p>
        <p><strong>A la recherche d’un patronyme et de formations diversifiées ...</strong>

            Dès lors le lycée-collège est en plein essor, comptant jusqu’à 1600 élèves en 1972, année qui vit la toiture
            de l’établissement soufflée par la tempête. Lui donner un patronyme serait de bon aloi mais aucun ne fait
            l’unanimité. Il faudra attendre 1988 pour qu’enfin, l’établissement prenne un nom fleurant bon la Bretagne,
            celui de son premier proviseur-fondateur Yves Kernanec, ainsi que le commémore la plaque apposée dans le
            hall d’entrée .
            Les années 80 et 90 peuvent être considérées comme celles du passage à la modernité, le Lycée-Collège
            restera un établissement d’enseignement général qui saura proposer à ses élèves des formations originales
            puisqu’en 1986 s’ouvrira une option « Cinéma-Audiovisuel », en 1997 une « Section européenne :anglais » en
            2000 et 2001 une « Section sportive : Equitation » et une « Section européenne : allemand ».</p>
        <p><strong>Travaux et évolutions...</strong><br><br>

            Dès le début des années 2000, l’idée de gros travaux fait son chemin : nouveau CDI, demi-pension refaite
            complétement à neuf (passage d’un service "à table" à un self), maison des lycéens et salle polyvalente.
            Ces travaux seront réalisés entre 2003 et 2005.
            En 2008, la décision de fermeture du collège est effective. Ne reste donc depuis lors sur le site que le
            lycée Kernanec.</p>
