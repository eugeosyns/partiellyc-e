        Informatique et Création Numérique
        L’ICN (informatique et création numérique) est un enseignement d’exploration proposé en classe de seconde au
        lycée
        Kernanec à compter de la rentrée 2018. En 1h30 chaque semaine et sans prérequis, cet enseignement propose une
        initiation
        à la programmation : conception d’un site web ou création d’un jeu simple. L’apprentissage se fait par la
        pratique, les
        séances se déroulent donc uniquement en salle informatique avec des petits exercices pour comprendre puis des
        mini-projets pour mettre en application ses connaissances.
        L’ICN pourra éventuellement être poursuivi en première et terminale en tant qu’option menant à une épreuve
        facultative
        au baccalauréat.

